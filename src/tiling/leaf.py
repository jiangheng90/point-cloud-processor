class Leaf:
    def __init__(
            self,
            depth,
            x_coord,
            y_coord,
            z_coord,
            number_of_x,
            number_of_y,
            number_of_z):
        self.depth = depth
        self.x = x_coord
        self.y = y_coord
        self.z = z_coord
        self.number_of_x = number_of_x
        self.number_of_y = number_of_y
        self.number_of_z = number_of_z
